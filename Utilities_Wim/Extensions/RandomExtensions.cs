		{ return random.NextDouble() * (max - min) + min; }
		
		public static DateTime NextDate(this Random random, DateTime min, DateTime max)
		{
			double totalDays = ((TimeSpan)(max - min)).TotalDays;
			double randDay = random.NextDouble(0, totalDays);
			DateTime randomDate = min.AddDays(randDay);
			return randomDate;
		}
		
		public static DateTime NextDate(this Random random)
		{ return random.NextDate(DateTime.MinValue, DateTime.MaxValue); }
		
		public static DateTime NextDate(this Random random, DateTime max)
		{ return random.NextDate(DateTime.MinValue, max); }
	}
